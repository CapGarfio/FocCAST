def get_comarcas(province_code):
    """Retorna lista de comarcas para una provincia dada, o lista vacía si no hay."""
    return COMARCAS.get(province_code, [])

def get_comarca_bbox(province_code, comarca_name):
    """Retorna el bbox de una comarca, o None si no se encuentra."""
    comarcas = get_comarcas(province_code)
    for name, bbox in comarcas:
        if name == comarca_name:
            return bbox
    return None